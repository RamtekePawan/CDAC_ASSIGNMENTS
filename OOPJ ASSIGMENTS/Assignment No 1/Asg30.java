/*
Write a program to find minimum and maximum number as well as 
to add two double numbers using methods of Double
 */
class Asg30{
    public static void main(String[] args){
        double n1 = 50.5;
        double n2= 100.5;

       System.out.println("Max : "+Double.max(n1, n2));
       System.out.println("Min : "+Double.min(n1, n2));
       System.out.println("Sum : "+Double.sum(n1, n2));
    }
}