/*
 * Write a program to find minimum and maximum number as well as 
to add two integer numbers using methods of Integer
 */
class Asg18{
    public static void main(String[] args){
        int n1 = 122;
        int n2=233;

       System.out.println("Max : "+Integer.max(n1, n2));
       System.out.println("Min : "+Integer.min(n1, n2));
       System.out.println("Sum : "+Integer.sum(n1, n2));
    }
}