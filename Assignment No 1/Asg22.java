/*
 * Write a program to find minimum and maximum number as well as 
to add two long numbers using methods of Long.
 */

 class Asg22{
    public static void main(String[] args){
        long n1 = 100;
        long n2= 200;

       System.out.println("Max : "+Long.max(n1, n2));
       System.out.println("Min : "+Long.min(n1, n2));
       System.out.println("Sum : "+Long.sum(n1, n2));
    }
}